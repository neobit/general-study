# Exercicio para ver as operações aritméticas do Python.

n1 = int(input('Digite um valor: '))
n2 = int(input('Digite outro valor: '))

print('A soma de {} e {} é: {}'.format(n1, n2, n1+n2))
print('A subtração de {} e {} é: {}'.format(n1, n2, n1-n2))
print('A multiplicação de {} e {} é: {}'.format(n1, n2, n1*n2))
print('A divisão de {} por {} é: {}'.format(n1, n2, n1/n2))
print('{} elevado a {}ª potência é: {}'.format(n1, n2, n1**n2))
print('A divisão inteira de {} por {} é: {}'.format(n1, n2, n1//n2))
